#define _CRT_SECURE_NO_WARNINGS 
#include <stdio.h>

int BackJoon1000(void)
{
    int num1;
    int num2;

    scanf("%d", &num1);    // 표준 입력을 받아서 변수에 저장
    scanf("%d", &num2);    // 표준 입력을 받아서 변수에 저장

    printf("%d\n", num1 + num2);    // 변수의 내용을 출력

    return 0;
}